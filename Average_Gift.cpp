using namespace std;
int main()
{
int t;
cin>>t;
while(t--){
    int n,x;
    cin>>n>>x;
    int arr[n];
    for(int i=0;i<n;i++)
    {
        cin>>arr[i];
    }
    sort(arr,arr+n);
    int mx=arr[n-1];
    int mn=arr[0];
    if(mn>x || mx<x)
    cout<<"NO"<<endl;
    else{
        cout<<"YES"<<endl;
    }
}
    return 0;
}